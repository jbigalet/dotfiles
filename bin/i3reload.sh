# combine global & local configuration files for i3
for f in i3/config i3/i3status.conf
do
  echo -e "# !!! DO NOT EDIT THIS FILE !!!\n# this file is automatically generated by xinitrc, by combining both local and global configurations" > ~/.$f.generated
  [[ -f ~/.$f ]] && cat ~/.$f >> ~/.$f.generated
  [[ -f ~/.this/$f ]] && cat ~/.this/$f >> ~/.$f.generated
done

i3-msg "restart"
